struct ListNode *getIntersectionNode(struct ListNode *headA, struct ListNode *headB) {
    if(headA == NULL || headB == NULL) return NULL;
    struct ListNode* curA = headA;
    struct ListNode* curB = headB;
    //遍历求长度
    int lenA = 1;
    int lenB = 1;
    while(curA)
    {
        curA = curA->next;
        lenA++;
    }
    curA = headA;
    while(curB)
    {
        curB = curB->next;
        lenB++;
    }
    curB = headB;
    //更长的链表头向前移动gap步
    int gap = abs(lenA - lenB);
    if(lenA > lenB)
    {
        while(gap--)
            curA = curA->next;
    }
    else
    {
        while(gap--)
            curB = curB->next;
    }
    //curA, curB 共同向后遍历，寻找相同节点
    while(curA && curB)
    {
        if(curA == curB)
            //找到共同节点
            return curA;
        curA = curA->next;
        curB = curB->next;
    }
    //未找到
    return NULL;
}